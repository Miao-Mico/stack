/**
 * @brief This function will swap the left-hand-side with the right-hand-side.
 *
 * @param lhs the pointer to the left-hand-side item
 * @param rhs the pointer to the right-hand-side item
 *
 * @return the error code
 */

extern inline errno_t
modify_sequence_control_swap(void *lhs, void *rhs,size_t len)
{
	assert(lhs);
	assert(rhs);
	assert(len);

	static void *tmp;

	if (NULL == (tmp = calloc(len, sizeof(char)))) {
		return -1;
	}

	memcpy(tmp, lhs, len);
	memcpy(lhs, rhs, len);
	memcpy(rhs, tmp, len);

	free(tmp);

	return 0;
}